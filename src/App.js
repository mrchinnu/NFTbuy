const ADDRESS="0xe1819B1EA5FE460a5f0D64330DF3475B79459C39";
var contract=null;
var account=null;
async function connectwallet() {
 
    if(window.ethereum){
      var web3= new Web3(window.ethereum);
      await window.ethereum.send('eth_requestAccounts');
      var accounts = await web3.eth.getAccounts();
      account= accounts[0];
      document.getElementById('wallet-address').textContent = account;
      contract =new web3.eth.Contract(ABI,ADDRESS);
    }
  }
async function mint(){
  if(window.ethereum){
        var _mintamnt = Number(document.querySelector("[name=amount]").value);
        var mintrate= Number(await contract.methods.cost().call());
        var totalamount = _mintamnt*mintrate;
      contract.methods.mint(account,_mintamnt).send({ from: account,value : String(totalamount)  });
      }
    }


